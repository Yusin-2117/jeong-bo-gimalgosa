#2차원 리스트

#리스트 명 = [[a1, a2, a3], [b1, b2, b3], [c1, c2, c3]]
#                [0]번          [1]번         [2]번
#            [0] [1] [2] ....

a = [['a1', 'a2', 'a3'],
     ['b1', 'b2', 'b3'],
     ['c1', 'c2', 'c3']]

print(a) #전체 출력

print(a[0]) # 0번째 리스트 출력

print(a[0][0]) # 0번째 리스트의 0번째 요소 출력