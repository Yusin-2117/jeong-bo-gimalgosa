#python 연산자들

#1. +더하기
print(3+4)
#2. -빼기
print(3-4)
#3. *곱하기
print(3*4)
#4. ÷나누기
print(3/4)
#5. 나머지
print(3%4)
#6. 제곱
print(3**4)
#7. 몫
print(3//4)

#산술 연산자의 우선 순위:
# -> 괄호 > 제곱 > 곱셈-나눗셈(左-右) > 덧셈-뺄셈(左-右)


#python 비교연산자들(판단)

#1. = 같다(같은가?T/F)
print(3==4)
#2. ≠ 같지 않다(같지 않은가?T/F)
print(3!=4)
#3. > 크다(큰가?T/F)
print(3>4)
#4. < 작다(작은가?T/F)
print(3<4)
#5. ≥ 크거나 같다(크거나 같은가?T/F)
print(3>=4)
#6. ≤ 작거나 같다(작거나 같은가?T/F)
print(3<=4)

#결과는 True or False(bool)로 나옴


#python 논리연산자들

#1. 그리고
print(True and False) #(둘 다 True(참)일 때 True(참))
#2. 또는
print(True or False) #(둘 중 하나라도 True(참)일 때 True(참))
#3. 아니면
print(not True) #(True(참)일 때 False(거짓))